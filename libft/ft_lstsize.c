#include"libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 1;
	if (lst == 0)
		return (0);
	lst = lst->next;
	while (lst != 0)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}
